package org.aurora.lovingmatching.view.compLibView;

import java.awt.CheckboxGroup;

import javax.swing.JCheckBox;

import org.aurora.lovingmatching.view.imageLibView.ImageFactory;

/**
 * @Description 复选框
 * @author m2o2o2d
 * 2014年5月15日下午2:29:46
 */
public class ACheckBox extends JCheckBox {
	
	private ImageFactory image;
	
	public ACheckBox(int x, int y,boolean state) {
		image = new ImageFactory();
		setBounds(x, y, 40, 40);
		setSelected(state);
		setOpaque(false);
		setIcon(image.getImage("11"));
		setSelectedIcon(image.getImage("12"));
	}

}
